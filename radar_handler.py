async def execute_radar_search(query, context: ContextTypes.DEFAULT_TYPE):
    """المرحلة 4: الدمج المتقدم مع أطلس المراجع العملاق وجلب النسخ المرفوعة فعلياً"""
    size = query.data.split(":")[1]
    category = context.user_data.get("radar_category", "literature")
    difficulty = context.user_data.get("radar_difficulty", "easy")
    
    pool = context.bot_data.get("db_conn")
    if not pool:
        await query.message.reply_text("❌ خطأ في الاتصال بقاعدة البيانات.")
        return

    # 1. جلب القائمة الموسعة من أطلس المراجع الضخم بناءً على خيارات المستخدم
    targeted_books = []
    if category in RADAR_ATLAS and difficulty in RADAR_ATLAS[category]:
        targeted_books = list(RADAR_ATLAS[category][difficulty])

    # خلط عشوائي كامل لتوليد اقتراحات متغيرة وغير متكررة أبداً للمستخدم
    random.shuffle(targeted_books)
    
    # نأخذ أكبر عينة بحث (حتى 15 كتاباً) لفحص توفر نسخها في الداتابيز دفعة واحدة لتسريع النتائج وضمان دقتها
    sample_books = targeted_books[:15]

    rows = []
    if sample_books and pool:
        try:
            async with pool.acquire() as conn:
                # صياغة استعلام ديناميكي ذكي يفحص توفر العناوين عبر ILIKE
                clauses = [f"file_name ILIKE $ {i+1}" for i in range(len(sample_books))]
                sql_where = " OR ".join(clauses)
                
                query_args = [f"%{book}%" for book in sample_books]
                
                sql = f"""
                SELECT file_id, file_name 
                FROM books 
                WHERE {sql_where}
                ORDER BY RANDOM() 
                LIMIT 5;
                """
                rows = await conn.fetch(sql, *query_args)
        except Exception as e:
            logger.error(f"Radar Massive SQL Execution Error: {e}")

    # 2. خط الأمان الخلفي: في حال لم تتوفر العينات الـ 15 المحددة بدقة داخل قاعدة ملفاتك
    if not rows and pool:
        backup_keywords = {
            "literature": ["رواية", "قصة", "روايات", "قصص", "رواية"],
            "philosophy": ["فلسف", "فكر", "منطق", "وعي", "العقل", "مقدمة"],
            "poetry": ["ديوان", "أشعار", "قصائد", "شعر"],
            "psychology": ["نفس", "سلوك", "عادات", "شخصية", "تطوير الذات"]
        }
        
        keywords = backup_keywords.get(category, ["كتاب"])
        where_clauses = [f"file_name ILIKE '%{k}%'" for k in keywords]
        sql_where_backup = " OR ".join(where_clauses)
        
        sql_fallback = f"""
        SELECT file_id, file_name FROM books 
        WHERE ({sql_where_backup}) 
        ORDER BY RANDOM() LIMIT 5;
        """
        try:
            async with pool.acquire() as conn:
                rows = await conn.fetch(sql_fallback)
        except Exception as e:
            logger.error(f"Radar Fallback System Error: {e}")

    # ضبط واجهة العرض النصي المنظمة والواضحة
    cat_titles = {"literature": "أدب وروايات 🔍", "philosophy": "فلسفة وفكر 🧠", "poetry": "شعر وديوان 📜", "psychology": "علم نفس وتطوير 💡"}
    diff_titles = {"easy": "سهل وسلس 🟢", "hard": "عميق وأكاديمي 🔴"}
    size_titles = {"short": "وجبة سريعة ⏱️", "long": "رحلة ممتدة 📚"}

    context.user_data["search_results"] = [dict(r) for r in rows]
    context.user_data["current_page"] = 0
    context.user_data["search_stage"] = f"🚀 رادار: {cat_titles.get(category)}"
    
    text_header = (
        f"🚀 **تمت التصفية الذكية والموسوعية بنجاح!**\n\n"
        f"تفضيلاتك المحددة:\n"
        f"**({cat_titles.get(category)} ➔ {diff_titles.get(difficulty)} ➔ {size_titles.get(size)})**\n\n"
        f"إليك أروع الترشيحات من أمهات الكتب المتاحة فوراً وعشوائياً في المكتبة الآن:"
    )
    await query.message.reply_text(text_header, parse_mode="Markdown")
    
    # استدعاء عرض الصفحات القياسي التفاعلي الخاص بالبوت لعرض النتائج بأزرار التحميل
    from search_handler import send_books_page
    await send_books_page(query, context)
